public class Relational_operator {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        int C = 10;
        System.out.println("(A=10 , B=5 , C=10)\nA==B: "+(A==B));
        System.out.println("A==C: "+(A==C));
        System.out.println("===============");
        System.out.println("(A=10 , B=5 , C=10)\nA!=B: "+(A!=B));
        System.out.println("A!=C: "+(A!=C));
        System.out.println("===============");
        System.out.println("(A=10 , B=5 , C=10)\nA>B: "+(A>B));
        System.out.println("A<B: "+(A<B));
        System.out.println("===============");
        System.out.println("(A=10 , B=5 , C=10)\nA>=B: "+(A>=B));
        System.out.println("A<=C "+(A<=C));
    }
}